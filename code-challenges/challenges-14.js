const LastWord = (str) => {
    // Find the index of the last space character
    const lastSpaceIndex = str.lastIndexOf(" ");
  
    // Use the slice method to extract the last word
    const lastWord = str.slice(lastSpaceIndex + 1);
  
    return lastWord;
  };
  
